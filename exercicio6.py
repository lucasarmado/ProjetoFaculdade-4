import math

numero = int(input('Digite um numero: '))

print('Seu antecessor é: ',numero-1)
print('Seu sucessor é..: ',numero+1)
print('Seu quadrado é..: ',numero**2)
print('Sua raiz é......: ',numero**(1/2))
raiz = math.sqrt(numero)
print('Sua raiz é......: ',raiz)

